function openModal() {
  var modal = document.getElementById("modalBackground");
  modal.style.display = "flex";
}

function closeModal() {
  var modal = document.getElementById("modalBackground");
  modal.style.display = "none";
}

function openCleaningModal() {
  var modal = document.getElementById("cleaningModalBackground");
  modal.style.display = "flex";
}

function closeCleaningModal() {
  var modal = document.getElementById("cleaningModalBackground");
  modal.style.display = "none";
}